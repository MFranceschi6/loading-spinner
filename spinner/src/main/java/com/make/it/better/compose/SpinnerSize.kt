package com.make.it.better.compose

import androidx.ui.unit.Dp
import androidx.ui.unit.dp

/**
 * Collection of common spinner dimensions,
 */
object SpinnerSize{

    /**
     * [Dp] of 20F.dp
     */
    val Small = 20F.dp

    /**
     * [Dp] of 40F.dp
     */
    val Medium = 40F.dp

    /**
     * [Dp] of 60F.dp
     */
    val Big = 60F.dp

    /**
     * [Dp] of 60F.dp
     */
    val Bigger = 80F.dp

    /**
     * Expands the spinner dimensions to the dimension of the element is applied
     */
    val FillElement = 0F.dp
}